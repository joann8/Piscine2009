#include "header.h"

int	check_inputs(char *argv)
{
	int i;

	i = 0;
	while (argv[i] != '\0')
	{
		if (i % 2 == 0)
		{
			if (argv[i] < '1' || argv[i] > '4')
				return (0);
		}
		else
		{
			if (argv[i] != ' ')
				return (0);
		}
		i++;
	}
	if (i != 31)
		return (0);
	return (1);
}
