char		*ft_strcapitalize(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A') && (str[i] <= 'Z'))
			str[i] = str[i] + 32;
		i++;
	}
	if ((str[0] >= 'a') && (str[0] <= 'z'))
		str[0] = str[0] - 32;
	i = 1;
	while (str[i] != '\0')
	{
		if ((str[i - 1] < '0') ||
				((str[i - 1] > '9') && (str[i - 1] < 'A')) ||
				((str[i - 1] > 'Z') && (str[i - 1] < 'a')) ||
				(str[i - 1] > 'z'))
		{
			if ((str[i] >= 'a') && (str[i] <= 'z'))
				str[i] = str[i] - 32;
		}
		i++;
	}
	return (str);
}
